//Datatypes in swift


//Character datatype
var gender:Character="M"
print("Your gender is :",gender)
//String datatype
var name:String="Aswath"
print("Your name is :",name)

//Integer datatype
var age:Int=21
print("Your age is :",age)

//Float datatype
var height:Float=157.8
print("Your height is :",height)

//Double datatype
var weight:Double=60.78
print("Your weight is :",weight)

//Boolean datatype
var marital:Bool=false
print("Your marital status :",marital)